# El método .split() en Python divide una cadena de texto en una lista de palabras, separándolas por defecto usando los espacios en blanco

# Se crea una variable llamada 'curso' y se le asigna una cadena de texto
curso = "cuantas horas le dedicas al dia a programar"

# Se imprime el resultado de aplicar el método .split() sobre la cadena
print(curso.split())



